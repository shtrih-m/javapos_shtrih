import com.shtrih.jpos.DIOUtils;
import com.shtrih.fiscalprinter.GS1Barcode;
import com.shtrih.fiscalprinter.SMFiscalPrinter;
import com.shtrih.jpos.fiscalprinter.FiscalPrinterImpl;

public class DIOGetCommandTimeout extends DIOItem {

    public DIOGetCommandTimeout(FiscalPrinterImpl service) {
        super(service);
    }

    public void execute(int[] data, Object object) throws Exception {
        DIOUtils.checkDataMinLength(data, 1);
        int[] outParams = (int[]) object;
        DIOUtils.checkDataMinLength(outParams, 1);

        int commandCode = data[0];
        outParams[0] = getPrinter().getCommandTimeout(commandCode);
    }

}
